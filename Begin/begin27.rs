// Дано число A. Вычислить A^8, использу вспомогательную 
// переменную и три операции умножения. Для этого последовательно 
// находить A^2, A^4, A^8. Вывести все найденные степени числа A. 

use std::io; 

fn main() { 
    // Получение значения A от пользователя 
    println!("Введите число A: "); 
    let mut a = String::new(); 
    io::stdin().read_line(&mut a)
        .expect("Не удалось прочитать строку"); 

    // Приведение полученных данных из строки в число 
    let a: f32 = a.trim().parse()
        .expect("Введите числовое значение"); 
    
    // Расчёт и вывод искомых значений 
    let x: f32 = a*a; 
    println!("A^2={}", x); 
    let x: f32 = x*x; 
    println!("A^4={}", x); 
    let x: f32 = x*x; 
    println!("A^8={}", x); 
} 
